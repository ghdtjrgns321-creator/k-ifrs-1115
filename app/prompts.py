# 모델별 프롬프팅 전략 (GPT-5 Prompting Guide 기반):
#   - gpt-4.1-mini (non-reasoning): 명시적 CoT, 규칙 열거, 예시 제공
#   - gpt-5-mini (reasoning): Output Contract 중심, Citation Locking, 모순 제거
#     → reasoning 모델: 목표와 제약만 명시, step-by-step 지시 금지
#     → Output Contract: 섹션·포맷·길이를 정확히 명시하면 가장 잘 따름
#     → Citation Locking: [참고 문서]에서만 인용, 형식 고정
