QUERY_MAPPING: dict[str, list[str]] = {
    k: [term.strip() for term in v.split(",") if term.strip()]
    for k, v in _RAW_QUERY_MAPPING.items()
}

# 빌드 타임 역매핑: 기준서 공식 용어 → 해당 용어를 참조하는 실무 용어 리스트
# generate.py에서 답변 시 실무 담당자 친화적 표현을 함께 제시하는 데 사용합니다.
INVERTED_MAPPING: dict[str, list[str]] = {}
for _practitioner_term, _official_terms in QUERY_MAPPING.items():
    for _term in _official_terms:
        INVERTED_MAPPING.setdefault(_term, []).append(_practitioner_term)


def _normalize_query(text: str) -> str:
    """QUERY_MAPPING 매칭 성공률을 높이기 위한 5단계 텍스트 정규화.

    기준서 원문의 특수문자 노이즈(가운뎃점, 괄호 영문, 하이픈 줄바꿈)를 제거합니다.
    칩 클릭이 아닌 자유 텍스트 입력에서도 QUERY_MAPPING exact match를 시도할 수 있게 합니다.
    """
    text = re.sub(r'\([A-Za-z\s]+\)', '', text)  # ① 괄호 영문 제거: "수익인식(Revenue Recognition)" → "수익인식"
    text = re.sub(r'[·•\-—]', ' ', text)          # ② 특수 기호 → 공백: "통제·이전" → "통제 이전"
    text = re.sub(r'-\n', '', text)                # ③ 하이픈 줄바꿈 제거
    text = re.sub(r'\s+', ' ', text)               # ④ 연속 공백 단일화
    return text.strip()                            # ⑤ 앞뒤 공백 제거


def _extract_keywords_llm(query: str) -> list[str]:
    """QUERY_MAPPING에 exact match가 없을 때만 LLM으로 관련 키 추출.

    환각 방지 설계:
      - LLM 출력을 QUERY_MAPPING 키 화이트리스트로 강제 필터링합니다.
      - 목록 외 단어를 LLM이 출력해도 코드 레벨에서 전량 제거됩니다.
      - "NO_MATCH" 응답 시 빈 리스트 반환 → 원본 쿼리 벡터 검색으로 graceful fallback.
    """
    from langchain_core.messages import HumanMessage
    from app.llm import get_front_llm

    key_list = list(QUERY_MAPPING.keys())
    prompt = (
        f"아래 목록에서 사용자 질문과 관련 있는 키를 JSON 배열로만 응답하세요.\n"
        f"목록에 없으면 반드시 [\"NO_MATCH\"]로만 응답하세요. 목록 외 단어는 절대 만들지 마세요.\n\n"
        f"[목록]\n{key_list}\n\n"
        f"[사용자 질문]\n{query}\n\n"
        f"응답 (JSON 배열만):"
    )
    try:
        response = get_front_llm().invoke([HumanMessage(content=prompt)])
        content = response.content.strip()
        start, end = content.find("["), content.rfind("]") + 1
        if start == -1 or end == 0:
            return []
        candidates = json.loads(content[start:end])
        if not isinstance(candidates, list) or candidates == ["NO_MATCH"]:
            return []
        # 화이트리스트 필터링 — LLM이 임의 생성한 키는 전량 제거
        return [k for k in candidates if k in QUERY_MAPPING]
    except Exception:
        return []


def _expand_query(query: str) -> str:
    """QUERY_MAPPING 딕셔너리로 실무 용어를 기준서 공식 용어로 확장합니다.

    정규화된 쿼리로 exact match를 시도하고, 매핑에 없으면 원본 그대로 반환합니다.
    콤마 대신 공백으로 연결하여 BM25 토크나이저/임베딩 모델의 콤마 노이즈를 방지합니다.
    """
    normalized = _normalize_query(query)
    expanded_list = QUERY_MAPPING.get(normalized, [normalized])
    return " ".join(expanded_list)


def _sort_by_section_and_score(docs: list[dict]) -> list[dict]:
    """섹션 순서로 그룹화하고, 섹션 내에서 Reranker 점수(rerank_score) 내림차순 정렬.

    rerank_score를 정렬 키로 쓰는 이유:
      - final_score는 임계값(0.05) 미만 문서가 모두 0.0으로 동점 처리되어 구별 불가
      - rerank_score는 Cohere 원시 점수이므로 모든 문서에 개별 점수가 부여됨
    """
    groups: dict[str, list[dict]] = {s: [] for s in _SECTION_ORDER}
    others: list[dict] = []

    for doc in docs:
        source = doc.get("source", "본문")
        if source in groups:
            groups[source].append(doc)
        else:
            others.append(doc)

    for source in groups:
        groups[source].sort(key=lambda d: d.get("rerank_score", 0.0), reverse=True)

    result: list[dict] = []
    for source in _SECTION_ORDER:
        result.extend(groups[source])
    result.extend(others)
    return result


def run_search(query: str, session_id: str, store: SessionStore) -> SearchResponse:
    """쿼리를 받아 관련 K-IFRS 문서를 검색하고 SearchResponse를 반환합니다.

    내부 흐름:
      1. _normalize_query  — 특수문자 정규화
      2. QUERY_MAPPING     — exact match 시도 (결정론적, LLM 없음)
         hit  → 공식 용어로 확장 후 검색
         miss → _extract_keywords_llm으로 화이트리스트 범위 내 키 추출 (1.5단계)
                LLM도 miss → 정규화된 원본 쿼리로 벡터/BM25 검색 (graceful fallback)
      3. search_all     — 하이브리드 검색 (Vector + BM25 + RRF + Window Boost)
      4. rerank_results — Cohere Reranker 전체 채점
      5. 섹션 정렬      — 섹션 순서 + Reranker 점수 내림차순
      6. store_search   — search_id → docs 캐시 (이후 /chat에서 재사용)

    결정론적 설계 원칙과의 타협점:
      - QUERY_MAPPING exact match hit: LLM 호출 없음 (완전 결정론적 유지)
      - miss 시에만 LLM 호출, 단 출력이 QUERY_MAPPING 키 화이트리스트로 강제 필터링됨
        → 환각으로 인한 오염된 검색 결과 위험 없음
    """
    normalized = _normalize_query(query)
    matched_list = QUERY_MAPPING.get(normalized)

    if matched_list:
        # 1.5단계 스킵: QUERY_MAPPING exact match hit
        expanded = " ".join(matched_list)
    else:
        # 1.5단계: LLM으로 QUERY_MAPPING 키 추출 시도
        matched_keys = _extract_keywords_llm(query)
        if matched_keys:
            # 복수 키의 공식 용어를 중복 없이 병합합니다.
            merged: list[str] = []
            seen: set[str] = set()
            for key in matched_keys:
                for term in QUERY_MAPPING[key]:
                    if term not in seen:
                        merged.append(term)
                        seen.add(term)
            expanded = " ".join(merged)
        else:
            # LLM도 miss: 정규화된 원본 쿼리로 벡터/BM25 검색 (graceful fallback)
            expanded = normalized if normalized else query

    retrieved = search_all(expanded, limit=BROWSE_LIMIT)
    # recall(검색)은 expanded 쿼리로, precision(채점)은 원본 쿼리로 분리
    # → 확장 키워드 나열로 인한 B25 과대평가 방지, 사용자 의도(B21)가 최상위로
    scored = rerank_results(query, retrieved, top_n=len(retrieved))
    sorted_docs = _sort_by_section_and_score(scored)

    search_id = str(uuid4())
    store.store_search(session_id, search_id, sorted_docs)

    return SearchResponse(
        standalone_query=expanded,
        search_id=search_id,
        docs=[_to_doc_result(doc) for doc in sorted_docs],
    )


def _to_doc_result(doc: dict) -> DocResult:
    """retriever 반환 dict를 API 응답용 DocResult로 변환합니다."""
    full = doc.get("full_content") or doc.get("content", "")
    return DocResult(
        source=doc.get("source", "본문"),
        hierarchy=doc.get("hierarchy", ""),
        title=doc.get("title", ""),
        content=doc.get("content", ""),
        full_content=full,
        related_paragraphs=doc.get("related_paragraphs", []),
        chunk_id=doc.get("chunk_id", ""),
        score=doc.get("rerank_score", 0.0),
        case_group_title=doc.get("case_group_title", ""),
    )
